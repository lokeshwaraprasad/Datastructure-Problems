 class Solution {
    public boolean checkPerfectNumber(int num) {
        // A perfect number must be greater than 1
        if (num <= 1) {
            return false;
        }
        
        int sum = 1; // Start with 1 because 1 is a divisor of all numbers
        
        // Find divisors up to the square root of the number
        for (int i = 2; i <= Math.sqrt(num); i++) {
            if (num % i == 0) { // If i is a divisor
                sum += i; // Add the divisor
                if (i != num / i) { // Add the corresponding divisor if it's different
                    sum += num / i;
                }
            }
        }
        
        // A number is perfect if the sum of divisors equals the number itself
        return sum == num;
    }
}
